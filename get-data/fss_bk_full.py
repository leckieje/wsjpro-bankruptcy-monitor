def FSS_call_chunk(company_codes_list, start, end, limit=1000):

    all_results = []
    list_length = len(company_codes_list)

    if list_length > limit:
        # Calculate the number of chunks needed
        num_chunks = (list_length + limit - 1) // limit  # Ceiling division

        for i in range(num_chunks):
            start_index = i * limit
            end_index = min((i + 1) * limit, list_length)
            chunk = company_codes_list[start_index:end_index]
            print(f"Processing chunk {i+1}/{num_chunks} with {len(chunk)} codes.")
            chunk_df = FSS_call(chunk, start, end)
            all_results.append(chunk_df)
    else:
        print(f"Processing single chunk with {list_length} codes.")
        single_df = FSS_call(company_codes_list, start, end)
        all_results.append(single_df)

    # Concatenate all dataframes into a single one
    if all_results:
        return pd.concat(all_results, ignore_index=True)
    else:
        return pd.DataFrame()

def save_json(out, file_path):
    # --- Conversion Logic ---
    # Create a new dictionary to store the serializable version
    serializable_out = {}

    for industry_key, industry_data_dict in out.items():
        # Each 'industry_data_dict' is the inner dictionary like {'ind_score_changes': ..., 'company_codes': ...}
        serializable_inner_dict = {}
        for sub_key, sub_value in industry_data_dict.items():
            if isinstance(sub_value, pd.DataFrame):
                # Convert DataFrame to JSON string
                serializable_inner_dict[sub_key] = sub_value.to_json(orient='records', indent=4)
            else:
                # Keep other values (lists, strings, numbers) as they are
                serializable_inner_dict[sub_key] = sub_value
        serializable_out[industry_key] = serializable_inner_dict

    # # --- Debugging check (optional, but good practice) ---
