def get_industries(df, limit=30):
  counts = df.IndustryDescriptor.value_counts()
  counts = counts.reset_index(drop=False)

  out = {'industry': [],
         'count': []
         }

  for i in range(len(counts)):
    if counts.loc[i, 'count'] >= limit:
      out['industry'].append(counts.loc[i, 'IndustryDescriptor'])
      out['count'].append(counts.loc[i, 'count'])

  return out

def get_firms(dic, df):

  dic['company_code'] = []
  dic['company_name'] = []
  dic['ticker'] = []
  dic['status'] = []
  dic['list_status'] = []

  for ind in dic['industry']:
    code = df[df.IndustryDescriptor == ind]
    dic['company_code'].append(code._CompanyCode.to_list())
    dic['company_name'].append(code.CompanyName.to_list())
    dic['ticker'].append(code.ExchangeTicker.to_list())
    dic['status'].append(code.Status.to_list())
    dic['list_status'].append(code.ListStatus.to_list())

  return dic

def FSS_industries(reg=['USA'], new=False, save=False, limit=30):
  codes = get_codes(reg, new, save)
  industries = get_industries(codes, limit)
  firms = get_firms(industries, codes)

  out = pd.DataFrame(firms)
  out['num_active'] = [x.count('A') for x in out['status']]
  out['num_listed'] = [x.count('L') for x in out['list_status']]

  return out

def FSS_call(firms, start, end):

  url = 'https://api.dowjones.com/fss/extractions/'

  headers = {
      'Content-Type': 'application/json',
      'user-key': os.environ['FACTIVA_SENTIMENT_API_KEY']
  }

  payload = {
      "query": {
          "companies": firms,
          "start": start,
          "end": end,
          "articles": False,
          "format": "json"
      }
  }

  if not end:
    del payload['query']['end']

  print(headers)
  print(payload)

  try:
    response = requests.post(url, headers=headers, data=json.dumps(payload))
    response.raise_for_status()  # Raise an exception for bad status codes
    print(f"Response text: {response.text}")
  except requests.exceptions.RequestException as e:
      print(f"An error occurred: {e}")
      if response is not None:
          print(f"Response status code: {response.status_code}")
          print(f"Response text: {response.text}")

  if response.status_code != 201:
      print("ERROR: An error occurred creating an extraction: " + response.text)
  else:
      resp = response.json()
      job_id = resp['extraction_id']
      currentState = resp['state']
      while currentState != 'JOB_STATE_DONE':
        sleep(10)
        get_job_url = f'{url}{job_id}'
        get_job_resp = requests.get(get_job_url, headers=headers)
        get_job_resp_dict = get_job_resp.json()
        print("Current state is: " + get_job_resp_dict['state'])
        # print(get_job_resp_dict)
        currentState = get_job_resp_dict['state']
      if 'destination' in get_job_resp_dict:
        self_link = get_job_resp_dict['destination']
        get_data_resp = requests.get(self_link[0], headers=headers)
        print(get_data_resp)
        lines = get_data_resp.text.strip().split('\n')
        to_df = []
        for line in lines:
          if line:
            try:
              json_obj = json.loads(line)
              to_df.append(json_obj)
            except json.JSONDecodeError as line_e:
              print(f"Error decoding line: {line_e} - Line: {line}")
  if len(to_df) > 0:
    return pd.DataFrame(to_df)
  else:
    print(f'No Data Returned: {to_df}')

def get_industry_changes(start, end, bottom=None): #this is the longer script below
  inds = FSS_industries()

  out = {}
  # start = '2025-01-01'
  # end = '2025-04-01'
  ind = inds[bottom:].reset_index()

  for i in range(len(ind)):
    sleep(20)
    firms = ind.loc[i, 'company_code']
    print(f"{len(firms)}: {firms}")
    get_firms = FSS_call(firms, start, end)

    mean_score = get_firms[['score_date', 'score']].groupby('score_date').mean().reset_index(drop=False)
    min_score = get_firms.groupby('score_date')['score'].min().to_list()
    max_score = get_firms.groupby('score_date')['score'].max().to_list()

    mean_score.index = pd.to_datetime(mean_score['score_date'])

    # daily percent change
    mean_score['daily_pct_change'] = mean_score['score'].pct_change()

    # weekly change
    mean_score['7_day_change'] = mean_score['score'].shift(7)
    mean_score['7_day_comp'] = mean_score['score_date'].shift(7)
    mean_score['weekly_pct_change'] = (mean_score['score'] - mean_score['7_day_change'])/ mean_score['7_day_change']

    # monthly change
    mean_score['30_day_change'] = mean_score['score'].shift(30)
    mean_score['30_day_comp'] = mean_score['score_date'].shift(30)
    mean_score['monthly_pct_change'] = (mean_score['score'] - mean_score['30_day_change'])/ mean_score['30_day_change']

    mean_score['min_score'] = min_score
    mean_score['max_score'] = max_score

    out[ind.loc[i, 'industry']] = {'ind_score_changes': mean_score, # pd.DataFrame
                                    'company_codes': firms, # list
                                    'company_scores': get_firms #pd.DataFramea
                                    }
    print(f"DONE: {ind.loc[i, 'industry']}")

  return out

